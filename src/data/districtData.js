// Quick lookup by ID
export const DISTRICT_MAP = DISTRICT_DATA.reduce((acc, d) => {
  acc[d.id] = d;
  return acc;
}, {});

// Get district by pincode
export function getDistrictByPincode(pincode) {
  return DISTRICT_DATA.find(d => d.pincode === pincode) || null;
}

// Get all high-risk districts (based on historical pattern)
export function getHighRiskDistricts() {
  return DISTRICT_DATA.filter(d =>
    ['HIGH', 'CRITICAL'].includes(d.historicalRisk)
  );
}

// Get shelter list for a district
export function getShelters(districtId) {
  return DISTRICT_MAP[districtId]?.shelters || [];
}

// Get emergency contact for a district
export function getEmergencyContact(districtId) {
  const d = DISTRICT_MAP[districtId];
  if (!d) return { phone: '112', controlRoom: '112' };
  return {
    phone: d.emergencyContact,
    controlRoom: d.controlRoom,
    collectorate: d.collectorate,
    ndrf: d.ndrf,
  };
}

